package com.itbank.model;

public class WishDTO {
	private int idx;
	private String wisher, wishCity, wishCountry, wishImg;
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getWisher() {
		return wisher;
	}
	public void setWisher(String wisher) {
		this.wisher = wisher;
	}
	public String getWishCity() {
		return wishCity;
	}
	public void setWishCity(String wishCity) {
		this.wishCity = wishCity;
	}
	public String getWishCountry() {
		return wishCountry;
	}
	public void setWishCountry(String wishCountry) {
		this.wishCountry = wishCountry;
	}
	public String getWishImg() {
		return wishImg;
	}
	public void setWishImg(String wishImg) {
		this.wishImg = wishImg;
	}
	
	

}
